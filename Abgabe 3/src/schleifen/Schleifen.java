package schleifen;

public class Schleifen {
    public static void main(String[] args) {
        for (int i = 0; i <= 5; i++) {
            System.out.print((10 + (i * 2)) + " ");
        }
        System.out.print("\n");
        for (int i = 0; i <= 5; i++) {
            System.out.print((50 + (i * 10)) + " ");
        }
        System.out.print("\n");
        for (int i = 0; i <= 5; i++) {
            System.out.print((99 - i) + " ");
        }
    }
}
